
public class code36_typecasting {
	
	public static void main(String[] args) {
		
		char a = 'D';
		int i = a;
		System.out.println(i);
		
		byte b = 10;
		int m = b;
		System.out.println(m);
		
		int c = 56;
		double d = c;
		System.out.println(d);
		
		byte v = 12;
		double q = v;
		System.out.println(q);
		
		byte k = 16;
		float f = k;
		System.out.println(f);
		
		float z = 99.98f;
		double w = z;
		System.out.println(w);
		
		int ij = 90;
		byte bj = (byte)ij;
		System.out.println(bj);
		
		double df = 899.456;
		float ff = (float)df;
		System.out.println(ff);
		
		int nk = 97;
		char ck = (char)nk;
		System.out.println(ck);
		int nm = ck -32;
		char cm = (char)nm;
		System.out.println(cm);
		
	}

}
